// For the Keil C51 compiler.

#ifndef __CONTROL_H__
#define __CONTROL_H__

#define NUL 0x00
#define SOH 0x01        // ^A
#define STX 0x02        // ^B
#define ETX 0x03        // ^C
#define EOT 0x04        // ^D
#define ENQ 0x05        // ^E
#define ACK 0x06        // ^F
#define BEL 0x07        // ^G
#define BS  0x08        // ^H
#define HT  0x09        // ^I
#define LF  0x0A        // ^J
#define VT  0x0B        // ^K
#define FF  0x0C        // ^L
#define CR  0x0D        // ^M
#define SO  0x0E        // ^N
#define SI  0x0F        // ^O
#define DLE 0x10        // ^P
#define DC1 0x11        // ^Q
#define DC2 0x12        // ^R
#define DC3 0x13        // ^S
#define DC4 0x14        // ^T
#define NAK 0x15        // ^U
#define SYN 0x16        // ^V
#define ETB 0x17        // ^W
#define CAN 0x18        // ^X
#define EM  0x19        // ^Y
#define SUB 0x1A        // ^Z
#define ESC 0x1B
#define FS  0x1C
#define GS  0x1D
#define RS  0x1E
#define US  0x1F
#define SP  0x20
#define DEL 0x7F

#endif
